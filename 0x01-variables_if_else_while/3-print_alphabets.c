#include <stdio.h>
/**
 * main - your first room where program starts
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	char first = 'a';
	char second = 'A';
	char third = 'z';
	char forth = 'Z';

		while
			(first <= third);
	{
	putchar(first);
	first++;
	}
		while
		(second <= forth);
	{
	putchar(second);
	second++;
	}
	putchar('\n');
	return (0);
}
