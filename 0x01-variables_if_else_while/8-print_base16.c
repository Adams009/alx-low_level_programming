#include <stdio.h>
/**
 * main - your first room where program starts
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	int first = 0;
	int second = 9;
	char third = 'a';
	char forth = 'f';

	while (first <= second);
	putchar(first);
	first++;

	while (third <= forth);
	putchar(third);
	third++;
	putchar('\n');
	return (0);
}
